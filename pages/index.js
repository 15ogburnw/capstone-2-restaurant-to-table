import Loading from "@/components/Loading";
import { useSupabaseClient } from "@supabase/auth-helpers-react";
import Head from "next/head";
import { useRouter } from "next/router";
import { useUser } from "@supabase/auth-helpers-react";

export default function Index() {
  const supabase = useSupabaseClient();
  const router = useRouter();

  const {
    data: { session },
    error,
  } = supabase.auth.getSession();

  if (session) {
    router.push("/dashboard");
  }
  if (session === null && !error) {
    router.push("/landing");
  }

  return (
    <>
      <Head>
        <title>Restaurant To Table</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
        <script src="https://developer.edamam.com/attribution/badge.js" defer />
      </Head>

      <main>
        {!session ? <Loading screen /> : null}
        {/* TODO: ERROR HANDLING */}
        {error ? (
          <div>
            <h1>
              Error Loading Session! Let&apos;s figure out how to take care of
              this with Sentry!
            </h1>
            {console.error(error)}
          </div>
        ) : null}
      </main>
    </>
  );
}
